// Напишите функцию fizzBuzz(num), которая принимает число и проверяет каждое число от 1 до num:
 // Если число делиться без остатка на 3 – выводит в консоль 'fizz' ,
 // если делится без остатка на 5 – выводит в консоль 'buzz' ,
 // если делится без остатка и на 3 , и на 5 - выводит в консоль 'fizzbuzz' .
function fizzBuzz(num) {
    for (let i = 1; i <= num; i++) {
        if (i % 3 === 0 && i % 5 === 0) {
            console.log("fizzbuzz");
        } else if (i % 3 === 0) {
            console.log("fizz");
        } else if (i % 5 === 0) {
            console.log("buzz");
        } else {
            console.log(i);
        }
    }
}
fizzBuzz(20);